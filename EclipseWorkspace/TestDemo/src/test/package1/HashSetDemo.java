package test.package1;
import java.util.*;

public class HashSetDemo {
	public static void main(String args[]) {
		
		Set<String> hashSet;
		hashSet = new HashSet<String>(5);
		
		hashSet.add("A");
		hashSet.add("LaLO");
		hashSet.add("8913");
		
		for(String item : hashSet) {
			System.out.print(item + " ");
		}
	}
}
